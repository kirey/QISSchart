
public class DemoDataObj {

	String company;
	String Robustness;
	String Conformity;
	
	public DemoDataObj(){}
	
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getRobustness() {
		return Robustness;
	}
	public void setRobustness(String robustness) {
		Robustness = robustness;
	}
	public String getConformity() {
		return Conformity;
	}
	public void setConformity(String conformity) {
		Conformity = conformity;
	}
	
	
}
